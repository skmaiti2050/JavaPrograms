import java.lang.*;
class Calculator
{
    public static void main(String args[])
    {
        int n1, n2, n3, sum, diff, prod;
        n1 = 5;
        n2 = 7;
        n3 = 9;
        sum = n1 + n2 + n3;
        diff = n1 - n2 - n3;
        prod = n1 * n2 * n3;
        System.out.println("Sum: " + sum);
        System.out.println("Difference: " + diff);
        System.out.println("Product: " + prod);
    }
}
